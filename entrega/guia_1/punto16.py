#Usar los métodos capitalize(), title(), swapcase() para formatear el valor del string
#“sometimes it is the people no one imagines anything of who do the things that no one
#can imagine.”

texto = """sometimes it is the people no one imagines anything of who do the things that no one
can imagine."""

texto = texto.capitalize()

print(texto)

texto = texto.title()

print(texto)

texto = texto.swapcase()

print(texto)
